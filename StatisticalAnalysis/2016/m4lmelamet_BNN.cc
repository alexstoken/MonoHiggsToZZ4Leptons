  double netfun(std::vector<double>& in, const double* w)
  {
    double x00 = in[0];
    double x01 = in[1];
    double x02 = in[2];
    double x;

    //Layer 1, Node 0
    x = w[0];
    x = x + w[1] * x00;
    x = x + w[2] * x01;
    x = x + w[3] * x02;
    double x10 = tanh(x);

    //Layer 1, Node 1
    x = w[4];
    x = x + w[5] * x00;
    x = x + w[6] * x01;
    x = x + w[7] * x02;
    double x11 = tanh(x);

    //Layer 1, Node 2
    x = w[8];
    x = x + w[9] * x00;
    x = x + w[10] * x01;
    x = x + w[11] * x02;
    double x12 = tanh(x);

    //Layer 1, Node 3
    x = w[12];
    x = x + w[13] * x00;
    x = x + w[14] * x01;
    x = x + w[15] * x02;
    double x13 = tanh(x);

    //Layer 1, Node 4
    x = w[16];
    x = x + w[17] * x00;
    x = x + w[18] * x01;
    x = x + w[19] * x02;
    double x14 = tanh(x);

    //Layer 1, Node 5
    x = w[20];
    x = x + w[21] * x00;
    x = x + w[22] * x01;
    x = x + w[23] * x02;
    double x15 = tanh(x);

    //Layer 2, Node 0
    x = w[24];
    x = x + w[25] * x10;
    x = x + w[26] * x11;
    x = x + w[27] * x12;
    x = x + w[28] * x13;
    x = x + w[29] * x14;
    x = x + w[30] * x15;
    double x20 = 1.0/(1.0+exp(-x));
    return x20;
  }
